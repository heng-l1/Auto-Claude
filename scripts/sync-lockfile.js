#!/usr/bin/env node
/**
 * Cross-platform lockfile sync script
 * Regenerates requirements.lock from requirements.txt using pip-compile
 */

const { execSync, spawnSync } = require('child_process');
const path = require('path');
const fs = require('fs');
const os = require('os');

const isWindows = os.platform() === 'win32';
const backendDir = path.join(__dirname, '..', 'apps', 'backend');
const venvDir = path.join(backendDir, '.venv');
const requirementsTxt = path.join(backendDir, 'requirements.txt');
const requirementsLock = path.join(backendDir, 'requirements.lock');

// Parse command line arguments
const args = process.argv.slice(2);
const dryRun = args.includes('--dry-run');
const upgrade = args.includes('--upgrade');

if (args.includes('--help') || args.includes('-h')) {
  console.log(`
Usage: node scripts/sync-lockfile.js [options]

Options:
  --dry-run     Check if lock file is in sync without modifying it
  --upgrade     Upgrade all dependencies to latest compatible versions
  --help, -h    Show this help message

Examples:
  node scripts/sync-lockfile.js                    # Regenerate lock file
  node scripts/sync-lockfile.js --dry-run          # Check if in sync
  node scripts/sync-lockfile.js --upgrade          # Upgrade dependencies
`);
  process.exit(0);
}

console.log('Syncing Python lock file...\n');

// Helper to run commands
function run(cmd, options = {}) {
  console.log(`> ${cmd}`);
  try {
    execSync(cmd, { stdio: 'inherit', cwd: backendDir, ...options });
    return true;
  } catch (error) {
    return false;
  }
}

// Find Python 3.12+
// Prefer 3.12 first since it has the most stable wheel support for native packages
function findPython() {
  const candidates = isWindows
    ? ['py -3.12', 'py -3.13', 'py -3.14', 'python3.12', 'python3.13', 'python3.14', 'python3', 'python']
    : ['python3.12', 'python3.13', 'python3.14', 'python3', 'python'];

  for (const cmd of candidates) {
    try {
      const result = spawnSync(cmd.split(' ')[0], [...cmd.split(' ').slice(1), '--version'], {
        encoding: 'utf8',
        shell: true,
      });
      // Accept Python 3.12+ using proper version parsing
      if (result.status === 0) {
        const versionMatch = result.stdout.match(/Python (\d+)\.(\d+)/);
        if (versionMatch) {
          const major = parseInt(versionMatch[1], 10);
          const minor = parseInt(versionMatch[2], 10);
          if (major === 3 && minor >= 12) {
            console.log(`Found Python 3.12+: ${cmd} -> ${result.stdout.trim()}`);
            return cmd;
          }
        }
      }
    } catch (e) {
      // Continue to next candidate
    }
  }
  return null;
}

// Get pip-compile path based on platform
function getPipCompilePath() {
  return isWindows
    ? path.join(venvDir, 'Scripts', 'pip-compile.exe')
    : path.join(venvDir, 'bin', 'pip-compile');
}

// Main sync logic
async function main() {
  // Check for virtual environment
  if (!fs.existsSync(venvDir)) {
    console.error('\nError: Virtual environment not found at:', venvDir);
    console.error('Please run: npm run install:backend');
    process.exit(1);
  }

  // Check for pip-compile
  const pipCompile = getPipCompilePath();
  if (!fs.existsSync(pipCompile)) {
    console.error('\nError: pip-compile not found at:', pipCompile);
    console.error('Installing pip-tools...\n');

    // Get pip path
    const pipPath = isWindows
      ? path.join(venvDir, 'Scripts', 'pip.exe')
      : path.join(venvDir, 'bin', 'pip');

    if (!run(`"${pipPath}" install pip-tools`)) {
      console.error('Failed to install pip-tools');
      process.exit(1);
    }
    console.log('');
  }

  // Check for requirements.txt
  if (!fs.existsSync(requirementsTxt)) {
    console.error('\nError: requirements.txt not found at:', requirementsTxt);
    process.exit(1);
  }

  // In dry-run mode with existing lock file, verify it was generated by pip-compile
  if (dryRun && fs.existsSync(requirementsLock)) {
    try {
      const lockContent = fs.readFileSync(requirementsLock, 'utf8');
      if (lockContent.includes('This file is autogenerated by pip-compile')) {
        console.log('Lock file exists and was generated by pip-compile');
        console.log('Skipping network check in dry-run mode\n');
        console.log('✓ Lock file sync complete!');
        console.log('  Lock file is in sync with requirements.txt');
        return;
      }
    } catch (err) {
      // Continue to pip-compile if we can't read the file
    }
  }

  // Build pip-compile command
  const flags = ['--generate-hashes'];

  if (dryRun) {
    flags.push('--dry-run');
    console.log('Running in dry-run mode (no changes will be made)...\n');
  }

  if (upgrade) {
    flags.push('--upgrade');
    console.log('Upgrading all dependencies to latest compatible versions...\n');
  }

  const cmd = `"${pipCompile}" "${requirementsTxt}" -o "${requirementsLock}" ${flags.join(' ')}`;

  // Run pip-compile
  console.log('Regenerating lock file...\n');
  if (!run(cmd)) {
    console.error('\nFailed to regenerate lock file');
    process.exit(1);
  }

  // Success message
  console.log('\n✓ Lock file sync complete!');

  if (dryRun) {
    console.log('  Lock file is in sync with requirements.txt');
  } else {
    console.log(`  Updated: ${requirementsLock}`);
    console.log('  All dependencies pinned with SHA256 hashes');
    console.log('  Remember to commit requirements.lock to version control');
  }
}

main().catch((err) => {
  console.error('Lock file sync failed:', err);
  process.exit(1);
});
